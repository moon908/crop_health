class NumberedCanvas(canvas.Canvas):
    """
    Two-pass canvas to add 'Page X of Y' footers and headers to every page except the cover page.
    """
    def __init__(self, *args, **kwargs):
        super().__init__(*args, **kwargs)
        self._saved_page_states = []

    def showPage(self):
        self._saved_page_states.append(dict(self.__dict__))
        self._startPage()

    def save(self):
        num_pages = len(self._saved_page_states)
        for state in self._saved_page_states:
            self.__dict__.update(state)
            self.draw_page_elements(num_pages)
            super().showPage()
        super().save()

    def draw_page_elements(self, page_count):
        # Skip header/footer on the cover page (Page 1)
        if self._pageNumber == 1:
            return

        self.saveState()
        self.setFont("Helvetica", 8)
        self.setFillColor(colors.HexColor("#64748b")) # Slate-500

        # Header
        self.drawString(54, 750, "CROP HEALTH ANALYSIS REPORT — SCIENTIFIC AI SUMMARY")
        self.setStrokeColor(colors.HexColor("#cbd5e1")) # Slate-300
        self.setLineWidth(0.5)
        self.line(54, 742, 558, 742)

        # Footer
        self.line(54, 45, 558, 45)
        page_text = f"Page {self._pageNumber} of {page_count}"
        self.drawRightString(558, 30, page_text)
        self.drawString(54, 30, "Generated by AI Crop Health Analysis System — Model v4.1 (ResNet-50)")
        
        self.restoreState()


class ReportGenerator:
    @staticmethod
    def _base64_to_temp_file(b64_str: str, suffix: str = ".jpg") -> str:
        """Decodes base64 string to a temporary file and returns the path."""
        if "," in b64_str:
            b64_str = b64_str.split(",")[1]
        data = base64.b64decode(b64_str)
        fd, path = tempfile.mkstemp(suffix=suffix)
        with os.fdopen(fd, 'wb') as tmp:
            tmp.write(data)
        return path

    @staticmethod
    def _generate_matplotlib_charts(result: Dict[str, Any], temp_dir: str) -> Dict[str, str]:
        """Generates matplotlib charts for the report."""
        paths = {}
        os.makedirs(temp_dir, exist_ok=True)

        # 1. Bar Chart: Disease Probability Distribution
        plt.figure(figsize=(6, 2.5))
        preds = result["disease_analysis"]["top_5_predictions"]
        diseases = [p["disease"] for p in preds][::-1]
        probs = [p["probability"] for p in preds][::-1]
        
        # Color palettes matching UI aesthetics
        colors_list = ['#10B981' if d == 'Healthy' else '#EF4444' if d == result["disease_analysis"]["disease_detected"] else '#3B82F6' for d in diseases]
        
        bars = plt.barh(diseases, probs, color=colors_list, height=0.55, edgecolor='#1e293b', linewidth=0.5)
        plt.title('Prediction Probability Distribution (%)', fontsize=10, fontweight='bold', pad=8, color='#1e293b')
        plt.xlabel('Probability (%)', fontsize=8, color='#475569')
        plt.xlim(0, 105)
        
        # Annotate values
        for bar in bars:
            width = bar.get_width()
            plt.text(width + 1.5, bar.get_y() + bar.get_height()/2, f'{width:.1f}%', 
                     va='center', ha='left', fontsize=7.5, fontweight='bold', color='#334155')

        plt.grid(True, axis='x', linestyle='--', alpha=0.3)
        plt.tight_layout()
        bar_path = os.path.join(temp_dir, "chart_prob.png")
        plt.savefig(bar_path, dpi=180)
        plt.close()
        paths["chart_prob"] = bar_path

        # 2. Donut Gauge: Plant Health Score
        plt.figure(figsize=(3, 3))
        health = result["crop_info"]["overall_health"]
        sizes = [health, 100 - health]
        colors_pie = ['#10b981', '#ef4444'] if health > 50 else ['#f59e0b', '#dc2626']
        
        plt.pie(
            sizes,
            colors=colors_pie,
            startangle=90,
            pctdistance=0.75,
            wedgeprops={'edgecolor': 'white', 'linewidth': 1.5}
        )
        # Draw center circle to make it a donut
        centre_circle = plt.Circle((0,0), 0.65, fc='white')
        fig = plt.gcf()
        fig.gca().add_artist(centre_circle)
        
        # Text in the middle
        plt.text(0, 0, f'{health}%\nHEALTH', horizontalalignment='center', verticalalignment='center',
                 fontsize=12, fontweight='bold', color='#1e293b')
        
        plt.title('Overall Plant Health Score', fontsize=10, fontweight='bold', pad=8, color='#1e293b')
        plt.tight_layout()
        health_path = os.path.join(temp_dir, "chart_health.png")
        plt.savefig(health_path, dpi=180)
        plt.close()
        paths["chart_health"] = health_path

        return paths

