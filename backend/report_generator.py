    @staticmethod
    def generate_csv(result: Dict[str, Any], csv_path: str):
        """Generates a CSV report summarizing the findings."""
        with open(csv_path, mode='w', newline='', encoding='utf-8') as f:
            writer = csv.writer(f)
            writer.writerow(["Report ID", result["id"]])
            writer.writerow(["Timestamp", result["timestamp"]])
            writer.writerow(["Crop Type", result["crop_info"]["crop_type"]])
            writer.writerow(["Overall Health Score", result["crop_info"]["overall_health"]])
            writer.writerow(["Disease Detected", result["disease_analysis"]["disease_detected"]])
            writer.writerow(["Scientific Name", result["disease_analysis"]["scientific_name"]])
            writer.writerow(["Model Confidence (%)", result["disease_analysis"]["confidence_score"]])
            writer.writerow(["Severity Level", result["disease_analysis"]["severity_level"]])
            writer.writerow(["Severity Percentage (%)", result["severity"]])
            writer.writerow([])
            
            # Recommendations
            writer.writerow(["Recommendation Parameter", "Action Plan Guidelines"])
            for key, val in result["recommendations"].items():
                writer.writerow([key.replace("_", " ").title(), val])

    @staticmethod
    def generate_json(result: Dict[str, Any], json_path: str):
        """Generates a JSON report (excluding huge base64 images for size efficiency)."""
        clean_result = {k: v for k, v in result.items() if not k.endswith("_base64")}
        with open(json_path, mode='w', encoding='utf-8') as f:
            json.dump(clean_result, f, indent=2)
